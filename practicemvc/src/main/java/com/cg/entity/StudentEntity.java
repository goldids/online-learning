package com.cg.entity;

public class StudentEntity {
	public String name;
	public int roll;
	public String dept;
	public String collage;
	public StudentEntity()
	{
		
	}
	public StudentEntity(String name, int roll, String dept, String collage) {
		super();
		this.name = name;
		this.roll = roll;
		this.dept = dept;
		this.collage = collage;
	}
}
